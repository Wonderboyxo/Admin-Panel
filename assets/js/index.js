const btn1 = document.getElementById("div1")
const kes1 = document.getElementById("ic1")

function mouseOver1(){
    kes1.style.color = "rgb(150,201,255)"
    btn1.style.borderLeftColor = "rgb(150,201,255)"
    btn1.style.borderLeftStyle = "solid";
}

function mouseOut1(){
    kes1.style.color = "rgb(160,171,148)"
    btn1.style.borderLeftColor = "none";
    btn1.style.borderLeftStyle = "none";
    
}

btn1.addEventListener("mouseover", mouseOver1);
btn1.addEventListener("mouseout", mouseOut1);

